                    O Museu Virtual de Biodiversidade de Angola nasce de uma parceria criada entre o ISCED - Huíla e a Animal Demography Unit (ADU)
                    na Universidade de Cape Town na África do Sul ao abrigo do Edital de Cooperação entre Angola e África do Sul do Ministério de
                    Ciência e Tecnologia da República de Angola, entidade que financiou o projecto.
                    <br><br>

                    O Museu Virtual de Biodiversidade de Angola (MVBA) provê uma plataforma para cidadãos cientistas darem o
                    seu contributo a projectos de Biodiversidade. Este conceito inovador foi desenvolvido pela Unidade de
                    Demografia Animal (ADU) da Universidade de Cape Town. Por acreditarmos neste projecto associamo-nos à
                    Universidade de Cape Town e desenvolvemos este projecto satélite para Angola.
                    <br><br><br>
                    O conceito generalizado de Museu que diga respeito a animais, implica um local onde podem ser
                    visitados animais embalsamados. O MVBA tem um conceito diferente.
                    <br><br><br>
                    A componente científica de qualquer Museu comtempla colecções em grandes quantidades de exemplares
                    da mesma espécie provenientes de diferentes variações e locais de colheita, todos cuidadosamente preservados
                    e etiquetados com indicação relativa à data e local da colheita. O MVBD também funciona
                    com estes critérios mas no lugar de ter os exemplares em gavetas ou frascos, tem imagens digitais organizadas numa
                    base de dados.
                    <br><br><br>
                    Convidamos todos os cidadãos a submeter fotografias digitais ao Museu Virtual, em conjunto com alguma informação básica.
                    <br><br><br>
                    A identificação das espécies pode ser feita pelos próprios observadores e será confirmada por uma painel de especialistas.
                    Mapas de distribuição para cada espécie estão disponíveis online e servem como ferramentas de conservação e educação.

                    <br><br><br>
                    Os registos do MVBA contribuem para a expansão das Bases de Dados de Distribuição para os diferentes grupos
                    taxonómicos; não só confirmam a presença de uma espécie em determinado espaço e tempo mas também providenciam novos
                    registos de distribuição para a espécies, o que muitas vezes conduz a extensões das variações conhecidas da espécie.
